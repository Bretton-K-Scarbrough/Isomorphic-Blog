<h1>About</h1>
<p>
An isomorphism is a structure-preserving transformation between two sets. Isomorphic transformations allow for multiple perspectives of the same mathematical object, without losing any nuance. Often, these transformations highlight new facets of their structure, giving deeper insight to the object of interest. Such transformations represent the Platolean ideal of how to learn to me. 
</p>
<br>
<p>
More often than not, in physics or mathematics, something that appears foreign can be understood through multiple perspectives. One of these perspectives, or multiple of them, can drastically reduces the complexity of the system. Though they may not be explicitly an isomorphism of the original system, the idea of changing how you observe a problem to aid in how you solve it is a constant theme. An example of a meaningful isomorphic transformation to me is the Fourier Transform. The Fourier transform serves as a wonderful perspective shift for differential equations, trivializing many difficult problems. Not all problems have isomorphisms that can outright solve the problem at hand, but they often times allow for a meaningful shift in perspective. These shifts in perspective allow individuals to grapple with the topic of study in an impactful way. 
</p>
<br>
<p>
Though the ultimate goal of this blog is to serve as an environment for me to share ideas I find meaningful or interesting, a topic of constant interest to me is that of eloquent explanations of complex ideas. To teach a topic is to learn the topic in a new way, I hope anyone reading this blog can learn with me. 
</p>
